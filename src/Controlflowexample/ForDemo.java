package Controlflowexample;

public class ForDemo {

	
		static int  i=9;//
	    //int i;
	    public   static void main(String[] args)
	    { 
	        //int i;
	        for(int i=0; i<=4; i++){     
	              System.out.println("Value of i is - " + i);              
	         }     
	         
	        System.out.println(i); //5, 9
	    }
	     
	     
	     
	    public static void printStaticI() 
	    {
	        //int i;
	        System.out.println(i);
	    }
	    
	    

	}


