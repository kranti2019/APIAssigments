package Controlflowexample;

public class Forexample {

	public static void main(String[] args) 
	{
		String[] company={"Infy","TCS","Cogni","Persistent"};
		for(String companyname:company)
		{
			System.out.println("Company name is  "+companyname);
		}
		for(int s=0;s<company.length;s++)
		{
		
			System.out.println("Company name is  "+company[s]);
		
	    }

}
}
