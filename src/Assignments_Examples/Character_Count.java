package Assignments_Examples;

public class Character_Count 
{

	public static void main(String[] args)
	{
		String str="SELENIUM";
		int count=0;
		for(int i=0;i<str.length();i++)
		{
			if(str.charAt(i)=='E')
				 
			{
			 
			
			count++;
			 
			}
			 
			
		}
		
		
		System.out.println("No. of Es in the string supplied are "+count);
			
		}
	}


